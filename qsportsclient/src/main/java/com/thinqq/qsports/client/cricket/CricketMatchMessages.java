package com.thinqq.qsports.client.cricket;

import com.google.gwt.i18n.client.Messages;

public interface CricketMatchMessages extends Messages{
	
	  @DefaultMessage("Batting")
	  String batting();
	  
	  @DefaultMessage("Bowling")
	  String bowling();
	  
	  @DefaultMessage("Batsman")
	  String batsman();
	  
	  @DefaultMessage("Bowled By")
	  String bowledBy();
	  
	  @DefaultMessage("Caught By")
	  String caughtBy();
	  
	  
	  @DefaultMessage("Runs")
	  String runs();
	  
	  @DefaultMessage("Balls Faced")
	  String ballsFaced();
	  
	  @DefaultMessage("Fours")
	  String fours();
	  
	  @DefaultMessage("Sixes")
	  String sixes();

}
