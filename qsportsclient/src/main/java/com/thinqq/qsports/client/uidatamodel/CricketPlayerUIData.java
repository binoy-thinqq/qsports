package com.thinqq.qsports.client.uidatamodel;

public class CricketPlayerUIData {

	private String name;
	private String batBallInfo;
	private String fieldingInfo;
	private String batOneliner;
	private String ballOneliner;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBatBallInfo() {
		return batBallInfo;
	}
	public void setBatBallInfo(String batBallInfo) {
		this.batBallInfo = batBallInfo;
	}
	public String getFieldingInfo() {
		return fieldingInfo;
	}
	public void setFieldingInfo(String fieldingInfo) {
		this.fieldingInfo = fieldingInfo;
	}
	public String getBatOneliner() {
		return batOneliner;
	}
	public void setBatOneliner(String batOneliner) {
		this.batOneliner = batOneliner;
	}
	public String getBallOneliner() {
		return ballOneliner;
	}
	public void setBallOneliner(String ballOneliner) {
		this.ballOneliner = ballOneliner;
	}
}
